
public class Case {
	
	public int petit;
	public int moyen;
	public int grand;
	public int mega;
	
	public Case(int p, int m, int g, int mega) {
		this.petit = p;
		this.moyen = m;
		this.grand = g;
		this.mega = mega;
	}
	public Case() {
		this.petit = 1;
		this.moyen = 1;
		this.grand = 1;
		this.mega = 1;
	}
}
